# Get User Input:
val1 = input("Enter a Value1: ")
val2 = input("Enter a Value2: ")
print(val1+val2)      # concatenate the both values not sum here.

# Type Casting:
num1 = int(input("Enter a Number1: "))
num2 = float(input("Enter a Number2: "))
print(num1+num2)      # sum the both numbers not concatenate here.

a = eval(input("Enter Numeric+Binary Data: "))     # i.e. 5, 6.5, 5+6j, 0b0101
print(a)
